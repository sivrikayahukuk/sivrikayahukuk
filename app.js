const normalizeText = (text) =>
  (text || "")
    .toString()
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/[^a-z0-9\s]/g, " ")
    .replace(/\s+/g, " ")
    .trim();

const levenshtein = (a, b) => {
  if (a === b) return 0;
  if (!a.length) return b.length;
  if (!b.length) return a.length;
  const prev = new Array(b.length + 1);
  const curr = new Array(b.length + 1);
  for (let j = 0; j <= b.length; j++) prev[j] = j;
  for (let i = 1; i <= a.length; i++) {
    curr[0] = i;
    for (let j = 1; j <= b.length; j++) {
      const cost = a[i - 1] === b[j - 1] ? 0 : 1;
      curr[j] = Math.min(curr[j - 1] + 1, prev[j] + 1, prev[j - 1] + cost);
    }
    for (let j = 0; j <= b.length; j++) prev[j] = curr[j];
  }
  return prev[b.length];
};

const scoreNameMatch = (queryNorm, candidateNorm) => {
  if (!queryNorm || !candidateNorm) return 0;
  if (queryNorm === candidateNorm) return 1;
  if (candidateNorm.includes(queryNorm) || queryNorm.includes(candidateNorm)) {
    return 0.9;
  }
  const distance = levenshtein(queryNorm, candidateNorm);
  const longest = Math.max(queryNorm.length, candidateNorm.length);
  const threshold = Math.min(3, Math.floor(longest / 4));
  if (distance <= threshold) {
    return Math.max(0.6, 1 - distance / longest);
  }
  return 0;
};

const runSanctionScreen = (query) => {
  const queryNorm = normalizeText(query);
  if (!queryNorm) return [];
  const hits = [];
  sanctionsData.forEach((record) => {
    const candidates = [record.name, ...(record.aliases || [])];
    let best = 0;
    let matchedAlias = null;
    candidates.forEach((candidate) => {
      const score = scoreNameMatch(queryNorm, normalizeText(candidate));
      if (score > best) {
        best = score;
        matchedAlias = candidate === record.name ? null : candidate;
      }
    });
    if (best > 0) {
      hits.push({ record, score: best, matchedAlias });
    }
  });
  const byEntity = new Map();
  hits.forEach((hit) => {
    const key = normalizeText(hit.record.name);
    if (!byEntity.has(key)) byEntity.set(key, []);
    byEntity.get(key).push(hit);
  });
  return hits
    .map((hit) => {
      const peers = byEntity.get(normalizeText(hit.record.name)) || [];
      const otherLists = peers
        .filter((p) => p.record.list !== hit.record.list)
        .map((p) => p.record.list);
      return {
        ...hit,
        corroborated: otherLists.length > 0,
        otherLists: Array.from(new Set(otherLists)),
      };
    })
    .sort((a, b) => b.score - a.score);
};

