const renderEvidence = (rows) => {
  const container = document.getElementById("evidence-rows");
  if (!container) return;
  container.innerHTML = "";
  if (!rows.length) {
    const empty = document.createElement("p");
    empty.className = "evidence-empty";
    empty.textContent = t("evidenceEmpty");
    container.appendChild(empty);
    return;
  }
  rows.forEach((row) => {
    const article = document.createElement("article");
    article.className = "evidence-row reveal";
    const sanctionSide = row.sanctions.length
      ? row.sanctions
          .map(
            (hit) => `
              <div class="evidence-fact-line">
                <strong>${escapeHtml(hit.record.list)}</strong>
                <span>${escapeHtml(hit.record.program)}</span>
                ${sanctionStatusBadge(hit)}
              </div>
            `
          )
          .join("")
      : `<div class="evidence-fact-line evidence-clear">${escapeHtml(t("evidenceNoSanction"))}</div>`;

    const factSide = row.facts.length
      ? row.facts
          .map(
            (fact) => `
              <div class="evidence-fact-line">
                ${verdictBadge(fact.status)}
                <span>${escapeHtml(fact.claim)}</span>
              </div>
            `
          )
          .join("")
      : `<div class="evidence-fact-line evidence-clear">${escapeHtml(t("evidenceNoFact"))}</div>`;

    const sources = [];
    if (row.sourcedFromFact) sources.push(t("evidenceLinkedFromFact"));
    if (row.sourcedFromSanction) sources.push(t("evidenceLinkedFromSanction"));

    article.innerHTML = `
      <header class="evidence-row-head">
        <h4>${escapeHtml(row.entity)}</h4>
        <span class="evidence-source">${sources.map((s) => escapeHtml(s)).join(" · ")}</span>
      </header>
      <div class="evidence-bridge">
        <div class="evidence-side evidence-structured">
          <p class="eyebrow">${escapeHtml(t("sanctionEyebrow"))}</p>
          ${sanctionSide}
        </div>
        <div class="evidence-link" aria-hidden="true">↔</div>
        <div class="evidence-side evidence-unstructured">
          <p class="eyebrow">${escapeHtml(t("factEyebrow"))}</p>
          ${factSide}
        </div>
      </div>
    `;
    container.appendChild(article);
  });
  observeReveal();
};

