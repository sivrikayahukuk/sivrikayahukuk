const renderExpertise = () => {
  const grid = document.getElementById("expertise-grid");
  grid.innerHTML = "";
  const filter = document.getElementById("expertise-filter").value;
  const query = document.getElementById("expertise-search").value.toLowerCase();

  expertiseData
    .filter((item) => filter === "all" || item.tag === filter)
    .filter((item) => item.title.toLowerCase().includes(query) || item.description.toLowerCase().includes(query))
    .forEach((item) => {
      const card = document.createElement("article");
      card.className = "expertise-card reveal";
      card.innerHTML = `
        <h3>${item.title}</h3>
        <p>${item.description}</p>
        <span class="eyebrow">${item.tag.toUpperCase()}</span>
      `;
      grid.appendChild(card);
    });
  observeReveal();
};

const renderInsights = () => {
  const grid = document.getElementById("insights-grid");
  grid.innerHTML = "";
  insightsData.forEach((item) => {
    const card = document.createElement("article");
    card.className = "insight-card reveal";
    card.innerHTML = `
      <div class="eyebrow">${item.category}</div>
      <h3>${item.title}</h3>
      <p>${item.description}</p>
      <small>${item.time}</small>
    `;
    grid.appendChild(card);
  });
  observeReveal();
};

const setLanguage = (lang) => {
  state.lang = lang;
  document.documentElement.lang = lang;
  const dict = translations[lang];
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    if (dict[key]) {
      el.placeholder = dict[key];
    }
  });
  langToggle.textContent = lang === "tr" ? "EN" : "TR";
};

const switchPage = (route) => {
  transition.classList.add("active");
  setTimeout(() => {
    sections.forEach((section) => {
      section.classList.toggle("active", section.id === route);
    });
    navLinks.forEach((link) => {
      link.classList.toggle("active", link.dataset.route === route);
    });
    transition.classList.remove("active");
  }, 300);
};

const handleHashChange = () => {
  const route = window.location.hash.replace("#/", "") || "home";
  switchPage(route);
};

const observeReveal = () => {
  const targets = document.querySelectorAll(".reveal");
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("visible");
        }
      });
    },
    { threshold: 0.2 }
  );

  targets.forEach((target) => observer.observe(target));
};

const animateStats = () => {
  document.querySelectorAll(".stat-number").forEach((stat) => {
    const target = Number(stat.dataset.target || 0);
    let current = 0;
    const step = Math.ceil(target / 40);
    const tick = () => {
      current += step;
      if (current > target) current = target;
      stat.textContent = current;
      if (current < target) requestAnimationFrame(tick);
    };
    tick();
  });
};

const setupMatrix = () => {
  const canvas = document.getElementById("matrix-canvas");
  const ctx = canvas.getContext("2d");
  const resize = () => {
    canvas.width = window.innerWidth;
    canvas.height = window.innerHeight;
  };
  resize();
  window.addEventListener("resize", resize);

  const columns = () => Math.floor(canvas.width / 20);
  let drops = Array.from({ length: columns() }, () => Math.random() * canvas.height);

  const draw = () => {
    ctx.fillStyle = "rgba(5, 7, 7, 0.2)";
    ctx.fillRect(0, 0, canvas.width, canvas.height);
    ctx.fillStyle = "rgba(42, 255, 159, 0.5)";
    ctx.font = "14px monospace";
    drops.forEach((y, i) => {
      const text = String.fromCharCode(0x30a0 + Math.random() * 96);
      const x = i * 20;
      ctx.fillText(text, x, y);
      drops[i] = y > canvas.height ? 0 : y + 20;
    });
    requestAnimationFrame(draw);
  };
  draw();
};

